    private void getDailySales() {
        try {
            prepare=conn.prepareStatement("SELECT * FROM dailysales");
            rs=prepare.executeQuery();
            dailySalesT.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            Logger.getLogger(DailySales.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
    private void getSum(){        
        int total=0;
        for (int i=0;i<dailySalesT.getRowCount();i++){
            total+=Float.parseFloat(dailySalesT.getModel().getValueAt(i, 2).toString());
        }
       totalsales.setText("Ksh."+String.valueOf(total));
    }
