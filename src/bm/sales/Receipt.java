    private void loadReceiptItems() {
       try {
			InputStream stream = getClass().getResourceAsStream("/projectReports/receipt.jrxml");
			JasperDesign jd = JRXmlLoader.load(stream);
			
			JRDesignQuery newQuery = new  JRDesignQuery();
      		        newQuery.setText("select order_no AS \"ORDER NO\",drink_name AS \"DRINK\",quantity AS \"QNTY\",unit_price AS \"S.PRICE\",quantity*unit_price AS \"TOTAL\",waiter_name AS \"WAITER\" from items_sold WHERE order_no='"+orderno+"'");
      		       jd.setQuery(newQuery);
      		
			JasperReport jr = JasperCompileManager.compileReport(jd);
			JasperPrint jp = JasperFillManager.fillReport(jr, null,conn);
			reportPanel.setLayout(new BorderLayout());
			JRViewer jrviewer = new JRViewer(jp);
			reportPanel.add(jrviewer);
			reportPanel.revalidate();
			reportPanel.repaint(50L);
		} catch (JRException e) {e.printStackTrace();}
    }

    

   
