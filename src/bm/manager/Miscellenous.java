    private void saveMiscellenous() {
        try {
            prepare=conn.prepareStatement("INSERT INTO misc_expenses(name,quantity,amount) VALUES(?,?,?)");
            prepare.setString(1, service.getText());
            prepare.setString(2, quantity.getText());
            prepare.setString(3, amount.getText());
            prepare.execute();
            JOptionPane.showMessageDialog(null, "Miscellenous Recorded Successfully", "Techflay Software Solutions", JOptionPane.INFORMATION_MESSAGE);
            service.setText("");
            quantity.setText("");
            amount.setText("");
        
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "The following Error Occured"+ex.getMessage(),"Techflay Software Solutions",JOptionPane.ERROR_MESSAGE);
        }
    }
