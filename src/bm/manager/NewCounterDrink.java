    private void drinknameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_drinknameFocusLost
        String dcategory=manage.getInsertedForm(drinkname.getSelectedItem().toString());
        if(dcategory.equals("Bottled")){
           cartons.setVisible(false);
           cartonsTxt.setVisible(false);
           
       }
       else{
          cartons.setVisible(true);
          cartonsTxt.setVisible(true);
          totalUnits.setEnabled(false);
          
       }
    }//GEN-LAST:event_drinknameFocusLost

