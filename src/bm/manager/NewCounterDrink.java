    private void cartonsTxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cartonsTxtKeyReleased
    int units=manage.getDrinkUnits(drinkname.getSelectedItem().toString());
    int cartonsupdate= Integer.parseInt(cartonsTxt.getText());
    int total_units=units*cartonsupdate;
    totalUnits.setText(String.valueOf(total_units));
    }//GEN-LAST:event_cartonsTxtKeyReleased

