    private void drinknameItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_drinknameItemStateChanged
       String myname=drinkname.getSelectedItem().toString();
       String returnedValue=manage.getInsertedForm(myname);
        if(returnedValue.equals("Bottled")){
            cartonsTxt.setEnabled(false);
            
        }
        else{
             cartonsTxt.setEnabled(true);
        }
    }//GEN-LAST:event_drinknameItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PreparedStatement prepare;
        DatabaseConfiguration dbc=new DatabaseConfiguration();
        
        int updateNumbervalue=Integer.parseInt(unitsT.getText());
        Connection conn;
        if(sellingP.isSelected()==true){
       
            if(spT.getText().equals("")){
               JOptionPane.showMessageDialog(null, "Price Field Cannot Be empty");
           }
           else{
            manage.updateSellingPrices(drinkname.getSelectedItem().toString(), spT.getText());
            JOptionPane.showMessageDialog(null, "Price of the drink has been updated Successfully");
           }
           
        }
        else if(quantity.isSelected()==true){
            try{
               conn=dbc.getConnection();
          
           
               prepare=conn.prepareStatement("UPDATE counter_drinks SET total_units=total_units+"+updateNumbervalue+"");
               prepare.executeUpdate();  
               JOptionPane.showMessageDialog(null, "The New Quantity Has Been Updated Accordingly");
            }
            catch(SQLException ex){
                ex.printStackTrace();
            }
           
        }
    }//GEN-LAST:event_jButton1ActionPerformed

